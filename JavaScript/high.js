function findFlag () {
    const result = eval(Boolean('-1'))
    switch (result) {
        case 'true': return '9a20ma'
        case 'false': return 'am20o4'
        case true: return 'znwo01'
        case false: return 'pw0mna'
        case '-1': return 'v039ma'
        case -1: return '13ma03'
    }
}